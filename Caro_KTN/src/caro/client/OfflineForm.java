    void InitGame() {

        Goban = new JGobanAI();
        Goban.setPreferredSize(new Dimension(500,500));
        
        Computer = new JComputerAI(Goban);
        
        panelCaro.setViewportView(Goban);

        Goban.Sakiyomi = false;
        Goban.Kinjite = false;

        Goban.init(500, 500);
        Goban.Initialize(Computer);
        Goban.Draw();
        
        GameState = GS_READY;
        
        Player = new int[3];
        
        firstButton.setEnabled(true);
        secondButton.setEnabled(true);
        
    }
    void StartGame(int FirstPlayer) {

        if(GameState!=GS_READY)
            return;

        Player[BLACK] = FirstPlayer;
        Player[WHITE] = (FirstPlayer==COMPUTER) ? HUMAN : COMPUTER;
        CurrentPlayer = BLACK;


        firstButton.setEnabled(false);
        secondButton.setEnabled(false);
        clearButton.setEnabled(true);
            //Buttons[i].disable();


        if(FirstPlayer==COMPUTER){
            playerType.setText("O");
            computerType.setText("X");
            Goban.Area[7][7] = 1;
            PlayComputer();
        } else {
            playerType.setText("X");
            computerType.setText("O");
            GameState = GS_PLAYUSER;
        }
    }
    
    void PlayComputer() {

        GPos pos = new GPos();

        putStatus("Computer now thinking...");

        switch(Computer.Think(CurrentPlayer,pos)) {

            case JComputerAI.THINK_OK:

                putStatus("It's now your turn.");
                CurrentPlayer = (CurrentPlayer == WHITE) ? BLACK : WHITE;
                if(Player[CurrentPlayer] == COMPUTER){
                    PlayComputer();
                } else {
                    GameState = GS_PLAYUSER;
                }
                break;

            case JComputerAI.THINK_FILLED:

                putStatus("Board is filled ! Draw game.");
                GameState = GS_END;
                break;

            case JComputerAI.THINK_WIN:

                putStatus("Computer Win !!!");
                GameState = GS_END;
                break;
        }
    }
    void putStatus(String strStt)
    {
        statusLabel.setText(strStt);
    }

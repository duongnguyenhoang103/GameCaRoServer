    private void panelCaroMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panelCaroMousePressed
        // TODO add your handling code here:
        int x = evt.getX() - Goban.getX();
        int y = evt.getY() - Goban.getY();
        
        if(GameState==GS_PLAYUSER && Player[CurrentPlayer]==HUMAN) {

            GPos pos = new GPos();
            if( !Goban.GetPos(x,y,pos) )
                return;
            
            switch(Goban.Check(CurrentPlayer, pos.x, pos.y)) {

                case JGobanAI.GC_OK:

                    CurrentPlayer = (CurrentPlayer == WHITE) ? BLACK : WHITE;
                    if(Player[CurrentPlayer] == COMPUTER){
                        GameState = GS_PLAYCOM;
                    }
                    Goban.Draw();
                    break;

                case JGobanAI.GC_ILLEGAL:

                    putStatus("Illegal postion ! Can't put");
                    break;

                case JGobanAI.GC_CANNOT:

                    putStatus("Aleady exist! Can't put");
                    break;

                case JGobanAI.GC_FILLED:

                    putStatus("Board is filled ! Draw game.");
                    GameState = GS_END;
                    break;

                case JGobanAI.GC_WIN:

                    putStatus("You Win !!!");
                    GameState = GS_END;
                    break;
            }
        }
    }//GEN-LAST:event_panelCaroMousePressed

